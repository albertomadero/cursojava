public class DataTypes {
    public static void main(String[] args) {
        // Tipos de datos primitivos
        int myInt = 33;

        double myDouble = 3.50;
        // Tambien existen float, long, byte

        char myChar = 'a';

        boolean myBoolean = true;

        // Saber el tipo de dato
        Boolean myBooleanClass = true;
        System.out.println(myBooleanClass.getClass().getSimpleName());
    }
}
