public class VariablesAndConstants {
    public static void main(String[] args) {
        // Variables
        // Definir el tipo de variable, asignar el nombre de la variablr y asignarle el valor
        String name = "Alberto";
        // Imprimir usando la variable
        System.out.println(name);
        // Cambiar valor a una variable
        name = "Albert";
        System.out.println(name);


        int age = 33;
        System.out.println(age);

        var email = "Albert@gmail.com";
        System.out.println(email);

        var year = 2025;

        // Constantes
        final String EMAIL = "Alberto@gmail.com";
        System.out.println(EMAIL);
    }

}
